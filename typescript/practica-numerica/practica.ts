// suma de numeros
var numero1:number = 45;
var numero2:number = 40;
var resultado:number = numero1 + numero2;
console.log(resultado);

// resta de numeros
var numero3:number = 45;
var numero4:number = 40;
var resultadoResta:number = numero3 - numero4;
console.log(resultadoResta);

//multiplicacion de numeros
var resultadoMultiplicacion:number = resultado * resultadoResta;
console.log(resultadoMultiplicacion);